//used to know which line a worked with contact is on, useful for changing data.
int contactLine;

    private void AddContactActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddContactActionPerformed
        try {
            //finds file and sets up file reader
            String fileName = "Contacts.txt";
            File jarFile = new File(MainPage.class.getProtectionDomain().getCodeSource().getLocation().toURI().getPath());
            String inputFilePath = jarFile.getParent() + File.separator + fileName;
            File f = new File(inputFilePath);
            FileReader fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            
            //Collect contact info
            String Name = AddFN.getText();
            String HPhone = AddHP.getText();
            String CPhone = AddCP.getText();
            String Email = AddE.getText();
            String Address = AddA.getText();
            
            //temporarily stores contact info
            List<String> contacts = new ArrayList<String>();
            String line;
            
            //reads all lines in file, then stores them.
            do{
                line = br.readLine();
                if (line==null){
                    break;
                }
                contacts.add(line);
            }while (line != null);
            
            //close writers.
            br.close();
            fr.close();
            
            //prepares for writing.
            FileWriter fw = new FileWriter(f);
            
            //adds new contacts to data in memory.
            contacts.add(Name+"#"+HPhone+"#"+CPhone+"#"+Email+"#"+Address);
            
            //copies data in memory to text file.
            for (String str : contacts){
                fw.write(str);
                fw.write("\n");
            }
            
            //closes writer
            fw.close();
            
            AddConfirm.setText(Name+" has been added to contacts");
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (URISyntaxException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_AddContactActionPerformed

