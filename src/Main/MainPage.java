    private void SearchContactActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchContactActionPerformed

        try {
            //prepares files and reader
            String fileName = "Contacts.txt";
            File jarFile = new File(MainPage.class.getProtectionDomain().getCodeSource().getLocation().toURI().getPath());
            String inputFilePath = jarFile.getParent() + File.separator + fileName;
            File f = new File(inputFilePath);
            FileReader fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            
            //gets input of name
            String name = SearchFN.getText();
            
            //variables for looping
            String line;
            String[] items;
            int liNum = 0;
            
            //loops through each line until the proper name is found then outputs.
            do{
                line = br.readLine();
                
                //ends if null line
                if (line == null){
                    break;
                }
                
                //splits line into individual data (storage is split by #'s
                items = line.split("#");
                
                //checks for the name
                if (items[0].equals(name)){
                    
                    //outputs all the text
                    SearchHP.setText(items[1]);
                    SearchCP.setText(items[2]);
                    SearchE.setText(items[3]);
                    SearchA.setText(items[4]);
                    
                    //sets line where data is located, if needed for a data change.
                    contactLine = liNum;
                    break;
                }
                //adds 1 to linum so that we know which line we are reading.
                liNum++;
                
            }while(line!= null);
            
            br.close();
            fr.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (URISyntaxException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SearchContactActionPerformed

    private void ChangeInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangeInfoActionPerformed
        FileReader fr = null;
        try {
            //prepare file
            String fileName = "Contacts.txt";
            File jarFile = new File(MainPage.class.getProtectionDomain().getCodeSource().getLocation().toURI().getPath());
            String inputFilePath = jarFile.getParent() + File.separator + fileName;
            File f = new File(inputFilePath);
            
            //prepare reading
            fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            
            //take data from file
            String Name = SearchFN.getText();
            String HPhone = SearchHP.getText();
            String CPhone = SearchCP.getText();
            String Email = SearchE.getText();
            String Address = SearchA.getText();
            
            //temporarily stores contact info
            List<String> contacts = new ArrayList<String>();
            
            //variables for looping
            String line;
            int i = 0;
            
            //reads all lines in file, then stores them.
            do{
                //reads line
                line = br.readLine();
                
                //checks if line is null
                if (line==null){
                    break;
                }
                
                //if this is the line we want to edit, we edit it.
                if (i == contactLine){
                    contacts.add(Name+"#"+HPhone+"#"+CPhone+"#"+Email+"#"+Address);
                }else{
                contacts.add(line);
                }
                i++;
            }while (line != null);
            
            //close writers.
            br.close();
            fr.close();
            
            //prepares writer
            FileWriter fw = new FileWriter(f);
            
            //copies data in memory to text file.
            for (String str : contacts){
                fw.write(str);
                fw.write("\n");
            }
            
            //closes writer
            fw.close();
            ChangeConf.setText("Change Confirmed");
            
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (URISyntaxException ex) {
            Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ChangeInfoActionPerformed

